from djitellopy import tello
from time import sleep #this adds the delays in the commands we want...

me = tello.Tello()
me.connect() #connection for tello...
print(me.get_battery())  #for indepth analysis press ctrl+ left-click to access all the builtin functions
#me.move_forward() #this tells us about how much in cm will the drone move in certain direction... but we need to control the movement...

me.takeoff()
# negative values are used for opposite directions
me.send_rc_control(0, 50, 0, 0) #if we write this command only it will start to move 100cm and land while moving 50cm/s for 2 seconds forward we need to reset this to 0,0,0,0
sleep(2)
me.send_rc_control(30, 50, 0, 0) #if we write this command only it will start to move 60cm and land while moving 30cm/s for 2 seconds right we need to reset this to 0,0,0,0
sleep(2)
me.send_rc_control(0, 0, 0, 0) #now this will stop the drone and then make it land..
me.land()




















































































































































